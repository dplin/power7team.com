			<span class="title_3" style="color:#333;font-weight:400;">Inside</span>			
			<ul>
				<li>Clear all unnecessary objects from furniture throughout the house. Keep decorative objects on the furniture restricted to groups of 1, 3, or 5 items. In general, a sparsely decorated home helps the buyer mentally 'move in' with their own things.</li>
				<li>Rearrange or remove some of the furniture in your home, if necessary. Many times home owners have too much furniture in a room. When it comes to selling your home, thin out overcrowded rooms to make the rooms appear larger.</li>
				<li>Clear all unnecessary objects from the kitchen countertops. If it hasn't been used for three months, put it away! Clear refrigerator fronts of messages, magnets, pictures, etc.</li>
				<li>In the bathroom, remove any unnecessary items from the countertops, tub, shower stall and commode top. Keep only the most necessary cosmetics, brushes, perfumes, etc., in one small group on the counter. Coordinate towels in one or two colors only.</li>
				<li>Take down, reduce, or rearrange pictures and objects on walls. Patch and paint all walls, if necessary.</li>
				<li>Review the house interior, room by room, and:
					<ol>
						<li>Paint any room needing paint.</li>
						<li>Clean carpet and drapes that need it.</li>
						<li>Clean windows.</li>
					</ol>
				</li>
				<li>If you need room to store extra possessions use the garage or rent a storage unit.</li>
				<li>Leave on certain lights during the day (your agent will show you which ones). During showings turn on ALL lights and lamps.</li>
				<li>Play light FM music every day in the house, for all viewings.</li>
			</ul>
