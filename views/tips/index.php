			<span class="title_3" style="color:#333;font-weight:400;">Outside</span>			
			<ul>
				<li>Go around the perimeter of the house and move all garbage cans, discarded wood scraps, extra building materials, etc., to the garage or, if applicable, take them to the dump. </li>
				<li>Check gutters and roof for dry rot and moss. Make sure they are swept & cleaned. </li>
				<li>Look at all plants. Plants are like children, they grow so fast. Prune bushes and trees. Keep plants from blocking windows: "You can't sell a house if you can't see it!" </li>
				<li>Remove any dead plants, weed all planting areas and put down fresh mulching material. </li>
				<li>Keep your lawn freshly cut, edged and fertilized during the growing season. </li>
				<li>Clear patios or decks of all small items, such as little planters, flower pots, charcoal, barbeques, toys, etc. </li>
				<li>Check the condition of the paint on your home, especially the trim and the front door. The first impression, or 'curb appeal,' is very important.</li>
			</ul>
